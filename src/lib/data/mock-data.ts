// Cache the drivers
const cachedDrivers: Driver[] = [...mockDrivers];

export const getDrivers = () => {
    return [...cachedDrivers];
};

// Function to update a driver's status
export const updateDriverStatus = (
    driverId: string,
    newStatus: DriverStatus
): Driver | null => {
    const driverIndex = cachedDrivers.findIndex(
        (driver) => driver.id === driverId
    );
    if (driverIndex === -1) return null;

    cachedDrivers[driverIndex] = {
        ...cachedDrivers[driverIndex],
        status: newStatus,
    };

    return cachedDrivers[driverIndex];
};

// Function to add a new driver
export const addDriver = (newDriver: Omit<Driver, "id">): Driver => {
    const driver: Driver = {
        id: `d${cachedDrivers.length + 1}`,
        ...newDriver,
    };

    cachedDrivers.unshift(driver);
    return driver;
};

